int		ft_iterative_factorial(int nb)
{
	int n;

	n = nb;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (n > 1)
	{
		n--;
		nb = nb * (n);
	}
	return (nb);
}
