int		ft_is_prime(int nb)
{
	int i;

	if (nb < 2)
		nb = nb * (-1);
	i = 2;
	while (nb > i)
	{
		while ((nb % i) != 0)
			i++;
		if (i != nb)
			nb = nb - 1;
	}
	return (i);
}
