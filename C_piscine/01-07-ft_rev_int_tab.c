void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int temp;

	i = 1;
	while (i <= (size / 2))
	{
		temp = tab[size - i];
		tab[size - i] = tab[i - 1];
		tab[i - 1] = temp;
		i++;
	}
}
