#include <unistd.h>

int		main(int argc, char *argv[])
{
	int i;
	int k;

	i = 0;
	k = 1;
	while (argc > 1)
	{
		while (argv[k][i])
			i++;
		write(1, argv[k], i);
		write(1, "\n", 1);
		i = 0;
		argc--;
		k++;
	}
}
